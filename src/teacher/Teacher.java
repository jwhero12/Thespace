package teacher;

public class Teacher {
	private int teacherID;
	private String teacherTitle;
	private String userID;
	private String teacherDate;
	private String teacherContent;
	private int teacherAvailable;
	
	public int getTeacherID() {
		return teacherID;
	}
	public void setTeacherID(int teacherID) {
		this.teacherID = teacherID;
	}
	public String getTeacherTitle() {
		return teacherTitle;
	}
	public void setTeacherTitle(String teacherTitle) {
		this.teacherTitle = teacherTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getTeacherDate() {
		return teacherDate;
	}
	public void setTeacherDate(String teacherDate) {
		this.teacherDate = teacherDate;
	}
	public String getTeacherContent() {
		return teacherContent;
	}
	public void setTeacherContent(String teacherContent) {
		this.teacherContent = teacherContent;
	}
	public int getTeacherAvailable() {
		return teacherAvailable;
	}
	public void setTeacherAvailable(int teacherAvailable) {
		this.teacherAvailable = teacherAvailable;
	}
	
	
}
